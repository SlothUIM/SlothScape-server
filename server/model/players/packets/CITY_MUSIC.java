    public static CITY_MUSIC getByMusicId(int id) {
        for (CITY_MUSIC song : values()) {
            if (song.getMusic() == id)
                return song;
        }
        return null;
    }
    public String getFormattedName() {
        return formatSongName(this.getName());
    }

    public static String formatSongName(String name) {
        if (name == null || name.isEmpty()) return "";
        String[] words = name.split("_");
        StringBuilder formatted = new StringBuilder();
        for (int i = 0; i < words.length; i++) {
            String word = words[i];
            formatted.append(Character.toUpperCase(word.charAt(0)))
                     .append(word.substring(1).toLowerCase());
            if (i < words.length - 1) {
                formatted.append(" ");
            }
        }
        return formatted.toString();
    }

    public static String getSongNameById(int musicId) {
        CITY_MUSIC song = getByMusicId(musicId);
        return song != null ? song.getName() : "Unknown";
    }
